"""
Оптимизированные промпты на основе результатов A/B тестирования
"""
from typing import Dict

class OptimizedPrompts:
    """Оптимизированные промпты для оценки чистоты"""
    
    @staticmethod
    def get_prompt_v2_optimized() -> str:
        """
        Оптимизированная версия V2 на основе A/B тестирования
        
        Улучшения:
        1. Более четкие критерии оценки
        2. Конкретные примеры недостатков
        3. Улучшенный формат ответа
        4. Оптимизированная длина (95 слов)
        """
        return """Ты эксперт по уборке. Сравни две фотографии одной квартиры и оцени чистоту.

КРИТЕРИИ ОЦЕНКИ:
1. Пыль и грязь на поверхностях (столы, полки, подоконники)
2. Пятна на полу, стенах, мебели, сантехнике
3. Беспорядок: разбросанные вещи, одежда, посуда
4. Мусор: остатки еды, упаковки, скопление пыли
5. Чистота окон, зеркал, стеклянных поверхностей

ЕСЛИ КОМНАТЫ РАЗНЫЕ → "Недостатки: невозможно оценить — несоответствие изображений. Оценка: 0. Рекомендации: загрузите корректные фото."

ФОРМАТ ОТВЕТА (строго соблюдать):
Недостатки: [конкретные проблемы через запятую].
Оценка: [число от 1 до 10, где 1 - очень грязно, 10 - идеально].
Рекомендации: [практичные советы по уборке, до 200 символов]."""

    @staticmethod
    def get_prompt_v2_1() -> str:
        """
        Версия V2.1 с улучшенной структурой
        
        Улучшения:
        1. Приоритизация недостатков
        2. Более четкая градация оценки
        3. Улучшенные примеры рекомендаций
        """
        return """Ты профессиональный оценщик чистоты. Проанализируй две фотографии квартиры.

АНАЛИЗИРУЙ ПО ПРИОРИТЕТАМ:
🔴 Критические: плесень, разливы, скопления мусора
🟡 Средние: пыль, разводы, умеренный беспорядок
🟢 Легкие: мелкие соринки, незначительные пятна

НЕСООТВЕТСТВИЕ: Если фото разных комнат → "Недостатки: невозможно оценить — несоответствие изображений. Оценка: 0. Рекомендации: загрузите корректные фото."

ФОРМАТ:
Недостатки: [перечисли через запятую, начиная с критических].
Оценка: [1-10, где: 1-3=очень грязно, 4-6=средне, 7-9=чисто, 10=идеально].
Рекомендации: [что сделать в первую очередь, 150 символов макс]."""

    @staticmethod
    def get_prompt_v2_2() -> str:
        """
        Версия V2.2 с шаблонами ответов
        
        Улучшения:
        1. Шаблоны для разных уровней чистоты
        2. Стандартизированные формулировки
        3. Оптимизация для парсинга
        """
        return """Оцени чистоту квартиры по двум фото (эталон vs текущее).

ШАБЛОНЫ ОТВЕТОВ:
• Отлично (9-10): "Недостатки: незначительные мелкие недочеты. Оценка: 9. Рекомендации: поддерживайте текущий уровень."
• Хорошо (7-8): "Недостатки: [2-3 конкретных проблемы]. Оценка: 7. Рекомендации: [2 действия]."
• Средне (5-6): "Недостатки: [3-5 проблем]. Оценка: 5. Рекомендации: [3-4 действия]."
• Плохо (1-4): "Недостатки: [5+ серьезных проблем]. Оценка: 3. Рекомендации: [полная уборка]."

НЕСООТВЕТСТВИЕ → "Недостатки: невозможно оценить — несоответствие изображений. Оценка: 0. Рекомендации: загрузите корректные фото."

ФОРМАТ (выбери подходящий шаблон):
Недостатки: [перечисли].
Оценка: [число].
Рекомендации: [текст]."""

    @staticmethod
    def get_prompt_v2_3() -> str:
        """
        Версия V2.3 с фокусом на скорости
        
        Улучшения:
        1. Минимальный промпт с сохранением качества
        2. Упрощенный формат ответа
        3. Фокус на ключевых критериях
        """
        return """Оцени чистоту: сравни 2 фото одной квартиры.

Критерии:
✓ Пыль/грязь ✓ Пятна ✓ Беспорядок ✓ Мусор ✓ Разводы

Разные комнаты? → "Недостатки: несоответствие. Оценка: 0. Рекомендации: загрузите корректные фото."

Ответ:
Недостатки: [через запятую].
Оценка: [1-10].
Рекомендации: [до 100 символов]."""

    @staticmethod
    def get_all_prompts() -> Dict[str, str]:
        """Возвращает все оптимизированные промпты"""
        return {
            "v2_optimized": OptimizedPrompts.get_prompt_v2_optimized(),
            "v2_1": OptimizedPrompts.get_prompt_v2_1(),
            "v2_2": OptimizedPrompts.get_prompt_v2_2(),
            "v2_3": OptimizedPrompts.get_prompt_v2_3(),
        }

    @staticmethod
    def get_prompt_stats() -> Dict[str, Dict]:
        """Статистика по промптам"""
        prompts = OptimizedPrompts.get_all_prompts()
        
        stats = {}
        for name, prompt in prompts.items():
            words = len(prompt.split())
            chars = len(prompt)
            lines = prompt.count('\n') + 1
            
            stats[name] = {
                "words": words,
                "characters": chars,
                "lines": lines,
                "tokens_approx": chars // 4,  # Приблизительная оценка токенов
                "density": words / chars if chars > 0 else 0
            }
        
        return stats

# Пример использования
if __name__ == "__main__":
    prompts = OptimizedPrompts()
    
    print("Оптимизированные промпты для оценки чистоты")
    print("=" * 60)
    
    for name, prompt in prompts.get_all_prompts().items():
        print(f"\n{name.upper()}:")
        print("-" * 40)
        print(prompt[:200] + "..." if len(prompt) > 200 else prompt)
        print(f"\nДлина: {len(prompt)} символов, {len(prompt.split())} слов")
    
    print("\n" + "=" * 60)
    print("СТАТИСТИКА ПРОМПТОВ")
    print("=" * 60)
    
    stats = prompts.get_prompt_stats()
    for name, data in stats.items():
        print(f"\n{name}:")
        print(f"  Слов: {data['words']}")
        print(f"  Символов: {data['characters']}")
        print(f"  Строк: {data['lines']}")
        print(f"  ~Токенов: {data['tokens_approx']}")